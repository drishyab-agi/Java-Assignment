package myArray;

public class ArrayElements {
	public static void main(String[] args)
    {
  
        int[] arr = { 20,45,66,78,98,100 };
  
        System.out.print("Elements of given array are: ");
 
        for (int i = 0; i < arr.length; i++) {
  
            System.out.print(arr[i] + " ");
        }
    }

}
